package entidades;

public class Distrito {

	    private int codDis;
	    private String nomDis;

	    
	    
	    public int getCodDis() {
	        return codDis;
	    }

	    public void setCodDis(int codDis) {
	        this.codDis = codDis;
	    }

	    public String getNomDis() {
	        return nomDis;
	    }

	    public void setNomDis(String nomDis) {
	        this.nomDis = nomDis;
	    }


}
